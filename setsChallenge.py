##SETS CHALLENGE
##Create a set.
DnDSet={'dwarf','elf','wizard','warlock'}
print(DnDSet)
##Add an item to a set using the add() method.
DnDSet.add('ogre')
print(DnDSet)
##Use the remove() method to take an item out of a set.
DnDSet.remove('warlock')
print(DnDSet)
##Use the difference() method on a set.
a={5,6,7,4,3}
b={1,2,3,4,5}
c=b.difference(a)
print(c)
